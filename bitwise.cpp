#include <iostream>
using namespace std;
int main() {
   //bitwise operators ( &,|,~ and,or,not)
    cout<<(10&4)<<endl;
    cout<<(11&9)<<endl;
    cout<<(10|4)<<endl;
    cout<<(11|4)<<endl;
    cout<<(~10)<<endl;
    cout<<(10^4)<<endl;
    cout<<(12^5)<<endl<<endl;
//left shift
    cout<<(9<<1)<<endl;
    cout<<(7<<1)<<endl;
    cout<<(9<<2)<<endl;
//right shift
    cout<<(2>>1)<<endl;
    cout<<(8>>2)<<endl;

    return 0;
}
